def show_waveplot(audio_path, label, gender):
    """Plot the time-domain waveform of an audio file."""
    x, sr = librosa.load(audio_path)
    plt.figure(figsize=(20, 6))
    librosa.display.waveshow(x, sr=sr)
    plt.title(f"Waveplot | Class: {label}  |  Gender: {gender}", fontsize=16)
    plt.xlabel("Time (s)")
    plt.ylabel("Amplitude")
    plt.tight_layout()
    plt.show()


def show_spectrogram(audio_path, label, gender):
    """Plot the short-time Fourier transform spectrogram (dB scale)."""
    x, sr = librosa.load(audio_path)
    X    = librosa.stft(x)
    Xdb  = librosa.amplitude_to_db(abs(X))
    plt.figure(figsize=(20, 6))
    librosa.display.specshow(Xdb, sr=sr, x_axis='time', y_axis='hz', cmap='plasma')
    plt.colorbar(format='%+2.0f dB')
    plt.title(f"Spectrogram | Class: {label}  |  Gender: {gender}", fontsize=16)
    plt.tight_layout()
    plt.show()


def show_zcr(audio_path, label, gender):
    """Plot the zero-crossing rate over time."""
    x, sr = librosa.load(audio_path)
    zcrs  = librosa.feature.zero_crossing_rate(x)
    total = librosa.zero_crossings(x).sum()
    print(f"Total zero crossings: {total}")
    plt.figure(figsize=(20, 5))
    plt.plot(zcrs[0], color='teal')
    plt.title(f"Zero Crossing Rate | Class: {label}  |  Gender: {gender}", fontsize=16)
    plt.xlabel("Frame")
    plt.ylabel("ZCR")
    plt.tight_layout()
    plt.show()

def show_spectral_centroids(audio_path, label, gender):
    """Plot spectral centroids overlaid on the waveform."""
    x, sr = librosa.load(audio_path)
    spectral_centroids = librosa.feature.spectral_centroid(y=x, sr=sr)[0]
    frames = range(len(spectral_centroids))
    t      = librosa.frames_to_time(frames)
    plt.figure(figsize=(20, 5))
    librosa.display.waveshow(x, sr=sr, alpha=0.4)
    plt.plot(t, normalize(spectral_centroids), color='r')
    plt.title(f"Spectral Centroids | Class: {label}  |  Gender: {gender}", fontsize=16)
    plt.xlabel("Time (s)")
    plt.ylabel("Normalized Centroid")
    plt.tight_layout()
    plt.show()


