# Ambil fitur (128 kolom pertama) dan reshape ke (n, 16, 8, 1)
# Kenapa (16, 8, 1)?
#   - 128 = 16 × 8 → kita anggap MFCC sebagai gambar 16×8 piksel
#   - 1 = channel (grayscale, bukan RGB)
#   - supaya bisa diproses oleh Conv2D yang biasanya menerima input 2D
