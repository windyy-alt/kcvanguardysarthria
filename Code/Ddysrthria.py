"""## MODELLING"""
# Arsitektur yang digunakan adalah kombinasi CNN dan GRU
# Setelah CNN mengekstrak fitur, GRU menangkap hubungan antar-frame
model = Sequential([

    # Input: tensor berukuran (16, 8, 1), MFCC yang sudah di-reshape
    InputLayer(input_shape=(16, 8, 1)),

    # Conv2D: cari pola lokal dalam grid MFCC menggunakan 32 filter 3×3
    # activation='relu': non-linearitas, matikan neuron negatif
    # padding='same': pertahankan ukuran spasial setelah konvolusi
    Conv2D(32, (3, 3), activation='relu', padding="same"),
    BatchNormalization(), # normalisasi output per batch → training lebih stabil & cepat
    MaxPooling2D(2, 2),   # downsample: kurangi dimensi spasial jadi setengah → (8, 4, 32)
    
    # Filter bertambah jadi 64 → fitur yang lebih kompleks dan abstrak
    Conv2D(64, (3, 3), activation='relu', padding="same"),
    BatchNormalization(),
    MaxPooling2D(2, 2), # ukuran jadi (4, 2, 64)

    # Reshape untuk GRU
    # GRU butuh input 2D: (timesteps, features)
    # Kita flatten dimensi terakhir: (4, 2, 64) → (4, 128)
    # Artinya: 4 timestep, masing-masing berisi 128 fitur
    Reshape((4, 128)),
    # 64 unit GRU, return_sequences=False → hanya output dari timestep terakhir
    GRU(64, return_sequences=False),
    # Dropout: saat training, 30% neuron dimatikan secara acak
    Dropout(0.3),

    Dense(32, activation='relu'),    # layer intermediate untuk kombinasi fitur
    Dense(1, activation='sigmoid')    # output: 1 neuron, nilai 0–1
])

# Compile model
# optimizer='adam'           : adaptive learning rate
# loss='binary_crossentropy' : loss function standar untuk klasifikasi biner
# metrics                    : pantau accuracy, AUC, precision, dan recall
model.compile(
    optimizer='adam',
    loss='binary_crossentropy',
    metrics=[
        'accuracy',
        AUC(name='auc'), # area under curve
        Precision(name='precision'), 
        Recall(name='recall')
    ]
)

# Simpan model ke file 'model.h5' setiap kali val_loss membaik (turun)
# save_best_only=True → hanya simpan yang terbaik, bukan setiap epoch
model_name = "model.h5"
