def feature_extraction(df):
    features = []
    valid_idx = []
    for i, record in tqdm(df.iterrows(), total=df.shape[0]):
        try:
            x, sr = librosa.load(record['filename'])
            mean_mfcc = np.mean(librosa.feature.mfcc(y=x, sr=sr, n_mfcc=128), axis=1)
            features.append(mean_mfcc)
            valid_idx.append(i)
        except EOFError:
            pass

    dataf = pd.DataFrame(features)
    dataf['class'] = df.loc[valid_idx, 'is_dysarthria'].values
    dataf['speaker_id'] = df.loc[valid_idx, 'filename'].str.extract(r'/(F[CM]\d+|MC\d+|FC\d+|M\d+|F\d+)_').values.flatten()
    return dataf

