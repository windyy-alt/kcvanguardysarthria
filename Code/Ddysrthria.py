def feature_extraction(df):
    """
    Ekstrak fitur MFCC dari setiap file audio dalam DataFrame.

    Prosesnya:
    - Load setiap file audio menggunakan librosa
    - Hitung 128 koefisien MFCC
    - Ambil rata-rata (mean) tiap koefisien sepanjang waktu
      → hasilnya vektor 128 angka per sampel audio
    - Simpan juga label kelas dan speaker ID

    Kenapa mean MFCC?
    Karena setiap audio punya durasi berbeda, sehingga jumlah frame-nya
    berbeda-beda. Dengan mengambil mean, kita dapat representasi
    fixed-size (128 dimensi) untuk semua audio.

    Kenapa n_mfcc=128?
    Karena lebih detail dibanding 40, akurasinya juga lebih tinggi
    """
    features = []
    valid_idx = []
    for i, record in tqdm(df.iterrows(), total=df.shape[0]):
        try:
            x, sr = librosa.load(record['filename'])    # load audio, sr default = 22050 Hz
            mean_mfcc = np.mean(librosa.feature.mfcc(y=x, sr=sr, n_mfcc=128), axis=1) # mean per koefisien (bukan per frame)
            features.append(mean_mfcc)
            valid_idx.append(i)
        except EOFError:
            # Skip file yang corrupt atau tidak bisa dibaca
            pass

    dataf = pd.DataFrame(features)
    dataf['class'] = df.loc[valid_idx, 'is_dysarthria'].values
    
    # Ekstrak speaker ID dari nama file, misal: "FC03_Session2..." → "FC03"
    dataf['speaker_id'] = df.loc[valid_idx, 'filename'].str.extract(r'/(F[CM]\d+|MC\d+|FC\d+|M\d+|F\d+)_').values.flatten()
    return dataf

